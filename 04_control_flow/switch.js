const month = "april";
switch (month) {
    case 1:
        console.log("January"); //shift+alt+down arrow to make duplicate
        break;
    case 2:
        console.log("Feburary");  
        break;
    case 3:
        console.log("March");  
        break;
    case 4:
        console.log("April");  
        break;

    default:
        console.log("default case  match");
        
        break;
}

